package musica;

public class Modificador {

    private float duracao;
    private float mod;
    private int tipo;

    public int getTipo() {
        return tipo;
    }

    public void setTipo(int tipo) {
        this.tipo = tipo;
    }

    public float getMod() {
        return mod;
    }

    public void setMod(float mod) {
        this.mod = mod;
    }

    public float getDuracao() {
        return duracao;
    }

    public void setDuracao(float duracao) {
        this.duracao = duracao;
    }
}
